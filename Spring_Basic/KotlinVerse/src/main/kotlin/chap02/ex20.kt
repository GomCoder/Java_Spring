fun main() {
    println("floor division and modulo")
    println(7.floorDiv(4))        // 1
    println((-7).floorDiv(4))     // -2
    println(7.floorDiv(-4))       // -2
    println((-7).floorDiv(-4))    // 1
    println(7.mod(4))             // 3
    println((-7).mod(4))          // 1
    println(7.mod(-4))            // -1
    println((-7).mod(-4))         // -3
}