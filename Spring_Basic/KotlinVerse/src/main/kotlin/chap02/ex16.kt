fun main() {
    val pipi = 0.314E1        // 3.14 = 0.314*10
    val pi100 = 0.314E3     // 314.0 = 0.314*1000
    val piOver100 = 3.14E-2 // 0.0314 = 3.14/100
    val thousand = 1E3      // 1000.0 = 1*1000

    println("pipi: $pipi")
    println("pi100: $pi100")
    println("piOver: $piOver100")
    println("thousand: $thousand")
}