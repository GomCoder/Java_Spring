fun main() {
    val byte: Byte = 1
    val int = 1
    val long = 1L
    val float = 1.5f
    val double = 1.5
    -byte   // -1: Int
    -int    // -1: Int
    -long   // -1: Long
    -float  // -1.5: Float
    -double // -1.5: Double

    println("byte: ${-byte}")
    println("int: ${-int}")
    println("long: ${-long}")
    println("float: ${-float}")
    println("double: ${-double}")
}