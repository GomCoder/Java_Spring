fun main() {
    println("division and modulo")
    println(7/4)           // 1
    println(-7/4)          // -1
    println(7/(-4))        // -1
    println((-7)/(-4))     // 1
    println(7%4)           // 3
    println(-7%4)          // -3
    println(7%(-4))        // 3
    println((-7)%(-4))     // -3
}